namespace Horizon.Framework.Services
{
    /// <summary>
    /// Enumeration for a message button setup.
    /// </summary>
    public enum MessageButtonSetup
    {
        /// <summary>
        /// A confirm button will be shown.
        /// </summary>
        Affirmitive,

        /// <summary>
        /// A confirm- and a cancel button will be shown.
        /// </summary>
        AffirmitiveAndNegative
    }
}